/**
 * >, <, >=, <=, ==, != 
 */

void main() {
  int num1 = 20;
  int num2 = 30;

  bool result = num1 > num2; //ture

  print(result);
  print("(num1 < num2 )${result}");
  print("(num1 <= num2)  ${(num1 <= num2)}");
  print("(num1 >= num2)  ${num1 >= num2}");
  print("(num1 == num2)  ${(num1 == num2)}");
  print("(num1 != num2)  ${(num1 != num2)}");
}

